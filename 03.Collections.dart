// List
List<int> numbers = [1, 2, 3];
numbers.add(4);
print(numbers[0]); // Access element

// Set
Set<String> fruits = {'apple', 'banana', 'orange'};
fruits.add('apple'); // Duplicates are ignored

// Map
Map<String, int> ages = {
  'Alice': 25,
  'Bob': 30,
};
ages['Charlie'] = 35;
print(ages['Alice']); // Access value
